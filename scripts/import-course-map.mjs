#!/usr/bin/env node
// Imports a single full-resolution course-map photo into R2 + sets
// events.course_map_image_key for the given event. Persists SQL to
// seeds/imports/ so re-seeds restore it.
//
// Usage:
//   node scripts/import-course-map.mjs --file /path/to/photo.jpg --event evt_cup2

import { spawn } from "node:child_process";
import { mkdir, writeFile, rm, copyFile } from "node:fs/promises";
import { join, dirname, basename } from "node:path";
import { tmpdir } from "node:os";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const PROJECT_ROOT = dirname(dirname(fileURLToPath(import.meta.url)));

const args = parseArgs(process.argv.slice(2));
const { file, event: eventId } = args;
if (!file || !eventId) {
  console.error("Usage: import-course-map.mjs --file PATH --event ID");
  process.exit(1);
}

const BUCKET = "cscc-media";
const DB_NAME = "cscc";
const MAX_WIDTH = 2400;

const tmp = await mkdir(join(tmpdir(), `cscc-course-${process.pid}`), { recursive: true });
const tmpDir = tmp ?? join(tmpdir(), `cscc-course-${process.pid}`);

const outPath = join(tmpDir, "course.webp");
console.log("Resizing…");
await sharp(file, { failOn: "none" })
  .rotate()                                        // honor EXIF orientation
  .resize({ width: MAX_WIDTH, withoutEnlargement: true })
  .webp({ quality: 84 })
  .toFile(outPath);

const key = `course-maps/${eventId}-${basename(file).replace(/[^A-Za-z0-9._-]/g, "_")}.webp`;
console.log(`Uploading to R2: ${key}`);
await wrangler(["r2", "object", "put", `${BUCKET}/${key}`,
  "--file", outPath, "--content-type=image/webp", "--local"]);

const importsDir = join(PROJECT_ROOT, "seeds", "imports");
await mkdir(importsDir, { recursive: true });
const sqlPath = join(importsDir, `course_map_${eventId}.sql`);
const sql = `-- Auto-generated by import-course-map.mjs at ${new Date().toISOString()}\nUPDATE events SET course_map_image_key = '${key}' WHERE id = '${eventId}';\n`;
await writeFile(sqlPath, sql);
console.log(`Wrote ${sqlPath}`);

await wrangler(["d1", "execute", DB_NAME, "--local", `--file=${sqlPath}`]);
await rm(tmpDir, { recursive: true, force: true });

console.log(`Done. Course map set on event ${eventId}.`);

function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i].startsWith("--")) {
      const key = argv[i].slice(2);
      const next = argv[i + 1];
      if (next && !next.startsWith("--")) { out[key] = next; i++; }
      else { out[key] = true; }
    }
  }
  return out;
}

function wrangler(args) {
  return new Promise((resolve, reject) => {
    const child = spawn("pnpm", ["wrangler", ...args], {
      stdio: ["ignore", "pipe", "pipe"],
    });
    let stderr = "";
    child.stderr.on("data", (d) => stderr += d.toString());
    child.on("exit", (code) => {
      if (code !== 0) reject(new Error(`wrangler ${args.join(" ")} failed (${code}): ${stderr}`));
      else resolve();
    });
  });
}
