#!/usr/bin/env node
// Backfill events for a past Scorekeeper season into the local DB.
//
// Usage:
//   node scripts/import-scorekeeper-season.mjs \
//     --series cscc2024 \
//     --season season_2024 \
//     [--location loc_bellis_fair]
//
// Pulls the public seriesinfo JSON, generates seeds/imports/season_<id>_events.sql
// with one INSERT per event, then applies it to local D1 via wrangler. The
// resulting file is committed so re-seeds restore the same rows; re-running
// the script regenerates it if Scorekeeper data changes.

import { spawn } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const PROJECT_ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const SCOREKEEPER_BASE = "https://scorekeeper.wwscc.org";
const DB_NAME = "cscc";

const args = parseArgs(process.argv.slice(2));
const { series, season, location } = args;
if (!series || !season) {
  console.error("Usage: import-scorekeeper-season.mjs --series cscc2024 --season season_2024 [--location loc_bellis_fair]");
  console.error("Omit --location to leave events with no venue (the public page hides the map).");
  process.exit(1);
}

console.log(`Fetching ${series}…`);
const url = `${SCOREKEEPER_BASE}/api2?items=seriesinfo&series=${encodeURIComponent(series)}&authtype=none`;
const res = await fetch(url);
if (!res.ok) {
  console.error(`Scorekeeper returned ${res.status}`);
  process.exit(1);
}
const data = await res.json();
if (!data.success || !data.seriesinfo?.events) {
  console.error(`No events in seriesinfo for ${series}`);
  process.exit(1);
}
const events = data.seriesinfo.events;
console.log(`Found ${events.length} event(s).`);

const esc = (v) => v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`;
const lines = [];
lines.push(`-- Auto-generated by import-scorekeeper-season.mjs at ${new Date().toISOString()}`);
lines.push(`-- Series: ${series}  Season: ${season}`);
lines.push(``);

for (const ev of events) {
  const shortId = ev.eventid.slice(0, 8);
  const id = `evt_${shortId}`;
  // Slug: <date>-sanitized-name to keep uniqueness across seasons.
  const slug = `${ev.date}-${ev.name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "")}`;
  const startAt = `unixepoch('${ev.date} 08:00:00-07:00')`;
  const endAt   = `unixepoch('${ev.date} 16:00:00-07:00')`;
  const eventType = ev.ispractice ? "autocross" : "autocross";
  const status = "completed";  // backfill is past events only
  const resultsUrl = `${SCOREKEEPER_BASE}/results/${series}/${shortId}`;
  const description = ev.ispractice
    ? "Practice day — no timing, pure seat time."
    : `Round of the ${season.replace("season_", "")} season at Scorekeeper series ${series}.`;
  // Strip the embedded HTML <p> wrappers from notes; we'll just use a clean default.

  // Without an explicit --location, leave the FK NULL — past seasons used
  // different venues we don't track yet. The free-text location_name shows
  // "Unknown venue" so cards aren't blank.
  const locationId = location ?? null;
  const locationName = ev.location || (location ? "Bellis Fair Mall" : "Unknown venue");
  const locationAddress = ev.location || null;

  lines.push(
    `INSERT OR REPLACE INTO events
       (id, slug, title, event_type, status, start_at, end_at,
        season_id, location_id, location_name, location_address,
        description_md, registration_url, results_url,
        run_groups_json, fee_member_cents, fee_nonmember_cents,
        created_at, updated_at, updated_by)
     VALUES (${esc(id)}, ${esc(slug)}, ${esc(ev.name)}, ${esc(eventType)}, ${esc(status)},
       ${startAt}, ${endAt},
       ${esc(season)}, ${esc(locationId)},
       ${esc(locationName)}, ${esc(locationAddress)},
       ${esc(description)},
       NULL, ${esc(resultsUrl)},
       NULL, NULL, NULL,
       unixepoch(), unixepoch(), 'scorekeeper-import');`,
  );
}

const importsDir = join(PROJECT_ROOT, "seeds", "imports");
await mkdir(importsDir, { recursive: true });
// File name keeps a stable prefix so re-imports overwrite the same file.
// Strip the redundant "season_" if the caller already passed a season id like "season_2024".
const filenameStem = season.replace(/^season_/, "season-");
const sqlPath = join(importsDir, `${filenameStem}_events.sql`);
await writeFile(sqlPath, lines.join("\n") + "\n");
console.log(`Wrote ${sqlPath}`);

console.log("Applying to local D1…");
await wrangler(["d1", "execute", DB_NAME, "--local", `--file=${sqlPath}`]);
console.log(`Done. Imported ${events.length} event(s) into season ${season}.`);

function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i].startsWith("--")) {
      const key = argv[i].slice(2);
      const next = argv[i + 1];
      if (next && !next.startsWith("--")) { out[key] = next; i++; }
      else { out[key] = true; }
    }
  }
  return out;
}

function wrangler(args) {
  return new Promise((resolve, reject) => {
    const child = spawn("pnpm", ["wrangler", ...args], {
      stdio: ["ignore", "pipe", "pipe"],
      cwd: PROJECT_ROOT,
    });
    let stderr = "";
    child.stderr.on("data", (d) => stderr += d.toString());
    child.on("exit", (code) => {
      if (code !== 0) reject(new Error(`wrangler ${args.join(" ")} failed (${code}): ${stderr}`));
      else resolve();
    });
  });
}
